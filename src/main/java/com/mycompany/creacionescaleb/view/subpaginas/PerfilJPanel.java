    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String estadoBoton = btnEditar.getText();
        if (estadoBoton.equals("EDITAR")){
            deshabilitarCampos(true);
            btnEditar.setText("GUARDAR");
            btnCancelar.setEnabled(true);
        } else {
            String newCorreo = textCorreo.getText();
            String newUsername = textNomUsuario.getText();
            String isValid = validarDatos();
            if (isValid == null) {
                if (!newCorreo.equals(dataPersona.getCorreo()) || !newUsername.equals(dataPersona.getUsername())) {
                    DaoUsuario dao = new DaoUsuarioImpl();
                    dataPersona.setCorreo(newCorreo);
                    dataPersona.setUsername(newUsername);
                    String update = dao.UsuarioUptdate(dataPersona);
                    if (update==null)
                        JOptionPane.showMessageDialog(this, "Actualizado correctamente.", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    else
                        JOptionPane.showMessageDialog(this, update, "AVISO", JOptionPane.ERROR_MESSAGE);
                }
                else
                    JOptionPane.showMessageDialog(this, "No se realizo ningun cambio.", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                
                deshabilitarCampos(false);
                btnEditar.setText("EDITAR");
            } else 
                JOptionPane.showMessageDialog(this, isValid, "AVISO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

