    // Validar campo de imputs
    private String validarCorreo(String correo) {
        String regex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Matcher matcher = Pattern.compile(regex).matcher(correo);
        return matcher.matches() ? null : "Correo no es válido";
    }
    
    private String validarNomUsuario(String nomUsuario) {
        String regex = "^[a-zA-Z0-9_]+$";
        Matcher matcher = Pattern.compile(regex).matcher(nomUsuario);
        return matcher.matches() ? null : "Nombre de usuario no es válido";
    }
    
    private String validarDatos() {
        String nombreCorreo, valUserName;
        nombreCorreo = validarCorreo(textCorreo.getText());
        valUserName = validarNomUsuario(textNomUsuario.getText());
        
        if (nombreCorreo == null && valUserName == null) {
            return null;
        } else {
            String errorString = String.format("ERROR: %s%s", nombreCorreo != null ? nombreCorreo + ", " : "", valUserName != null ? valUserName + ", " : "");
            return errorString.substring(0, errorString.length() - 2) + ".";
        }
    }

