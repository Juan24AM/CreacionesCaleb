    private void loadUsuarios() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Borrar todas las filas existentes en el modelo
            model.setRowCount(0);

            // Agregar las nuevas filas al modelo
            daousuario.usuarioLista().forEach((u) -> model.addRow(new Object[]{u.getId(), u.getDni(), u.getNombres(), u.getPaterno(), u.getMaterno(), u.getGenero(), u.getFeCreacion(), u.getEdad(), u.getCorreo(), u.getFeCreacion(), u.getTipoUsuario(), u.getUsername(), u.getDniResponsable()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
