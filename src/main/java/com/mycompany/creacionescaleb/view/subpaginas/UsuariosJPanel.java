    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRows().length < 1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar uno o mas ventas a eliminar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            for (int i : jTable1.getSelectedRows()) {
                try {
                    String estadoEliminacion = daousuario.UsuarioDelete((int) jTable1.getValueAt(i, 0), null);
                    if (estadoEliminacion == null) {
                        model.removeRow(i);
                        javax.swing.JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, "No se elimino el usuario.", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            try {
                int idVentaEdit = (int) jTable1.getValueAt(selectedRow, 0);
                invocarBotonEditar(idVentaEdit);
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar el usuario a editar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void invocarBotonEditar(Integer idusuario) {
        usuarioedit = daousuario.UsuarioGet(idusuario, null);
        // Crear una instancia de RegistroInventarioJPanel
        RegistroUsuarioJPanel invt = new RegistroUsuarioJPanel(usuarioedit, true);

        // Mostrar la instancia en el Dashboard
        Dashboard.showJPanel(invt);
        
        // Establecer la foto del producto en la misma instancia
        SwingUtilities.invokeLater(() -> {
            invt.setImageEdit();
        });
    }
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Crear una instancia de RegistroInventarioJPanel
        Usuario userResponsable = Dashboard.getUsuarioLogin();
        RegistroUsuarioJPanel invt;
        if (userResponsable.getNombres() != null) {
            invt = new RegistroUsuarioJPanel(userResponsable, false);
        } else {
            invt = new RegistroUsuarioJPanel();
        }
        

        // Mostrar la instancia en el Dashboard
        Dashboard.showJPanel(invt);
    }//GEN-LAST:event_jButton3ActionPerformed


